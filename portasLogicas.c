#include <stdio.h>
#include "pico/stdlib.h"
#include "hardware/adc.h"
#include <string.h>
#include <stdlib.h>
#include "inc/ssd1306.h"
#include "hardware/i2c.h"

// Definições de pinos
#define LED_R 13
#define LED_G 11
#define botao_A 5
#define botao_B 6
#define I2C_SDA 14
#define I2C_SCL 15
#define TAMANHO_BITMAP 1024 // Tamanho do bitmap (128x64 pixels)

// Protótipo das funções para deixar o código mais organizado e o main ficar em cima
void PortaAND();
void PortaOR();
void PortaXOR();
void PortaNAND();
void PortaNOR();
void PortaXNOR();
void PortaNOT();
void show_message(char *text[]);

// Declaração dos bitmaps como extern (serão definidos no final do código)
extern const uint8_t bitmap_AND[];
extern const uint8_t bitmap_OR[];
extern const uint8_t bitmap_NOT[];
extern const uint8_t bitmap_NAND[];
extern const uint8_t bitmap_NOR[];
extern const uint8_t bitmap_XOR[];
extern const uint8_t bitmap_XNOR[];

// Variáveis globais
uint8_t ssd[ssd1306_buffer_length];
int indiceOpcao = 0;

// Área de renderização
struct render_area frame_area =
{
    .start_column = 0,
    .end_column = ssd1306_width - 1,
    .start_page = 0,
    .end_page = ssd1306_n_pages - 1
};

//Mensagem inicial
char *telaInicial[] = { "Portas logicas", "-", "Mova o joystick", "para visualizar"};

int main()
{
    stdio_init_all();

    adc_init();
    adc_gpio_init(26); //eixo X
    adc_gpio_init(27); //eixo Y

    // Configurações padrões
    gpio_init(botao_A);
    gpio_set_dir(botao_A, GPIO_IN);
    gpio_pull_up(botao_A);
    gpio_init(botao_B);
    gpio_set_dir(botao_B, GPIO_IN);
    gpio_pull_up(botao_B);

    gpio_init(LED_R);
    gpio_set_dir(LED_R, GPIO_OUT);
    gpio_init(LED_G);
    gpio_set_dir(LED_G, GPIO_OUT);

    i2c_init(i2c1, ssd1306_i2c_clock * 1000);
    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA);
    gpio_pull_up(I2C_SCL);

    ssd1306_init(); // Inicializa o display
    calculate_render_area_buffer_length(&frame_area); // Calcula o tamanho do buffer
    memset(ssd, 0, ssd1306_buffer_length); // Limpa o buffer
    show_message(telaInicial); // Exibir mensagem inicial
    sleep_ms(4000); // Espera 4 segundos com a mensagem na tela

    ssd1306_t ssd_bm; // Aqui é criada uma variável do tipo ssd1306_t, que é a estrutura que contém as informações do display
    ssd1306_init_bm(&ssd_bm, 128, 64, false, 0x3C, i2c1); // Inicializa a estrutura com as informações do display:largura, altura, VCC externo (false), endereço I2C (0x3C), e o barramento I2C (i2c1)
    ssd1306_config(&ssd_bm); // Configura o display com as informações da estrutura

    while (true)
    {
        adc_select_input(0); // Seleciona o canal 0
        uint adc_y_raw = adc_read(); // Lê o valor do eixo Y

        if (adc_y_raw > 3500) // Verifica se o joystick foi movido para cima
        {
            indiceOpcao = (indiceOpcao - 1 + 7) % 7; // Se sim, decrementa o índice da opção
            sleep_ms(250); // Espera 250ms para evitar múltiplas leituras
        }
        else if (adc_y_raw < 150) // Verifica se o joystick foi movido para baixo
        {
            indiceOpcao = (indiceOpcao + 1) % 7; // Se sim, incrementa o índice da opção
            sleep_ms(250); // Espera 250ms para evitar múltiplas leituras
        }
        
        switch (indiceOpcao) { // Verifica em qual índice/página está, ou seja, qual porta lógica está selecionada
            case 0:
                memcpy(ssd, bitmap_AND, TAMANHO_BITMAP); // Copia o bitmap da porta AND para o buffer
                PortaAND(); // Chama a função da porta AND
                break;
            case 1:
                memcpy(ssd, bitmap_OR, TAMANHO_BITMAP);
                PortaOR();
                break;
            case 2:
                memcpy(ssd, bitmap_NOT, TAMANHO_BITMAP);
                PortaNOT();
                break;
            case 3:
                memcpy(ssd, bitmap_NAND, TAMANHO_BITMAP);
                PortaNAND();
                break;
            case 4:
                memcpy(ssd, bitmap_NOR, TAMANHO_BITMAP);
                PortaNOR();
                break;
            case 5:
                memcpy(ssd, bitmap_XOR, TAMANHO_BITMAP);
                PortaXOR();
                break;
            case 6:
                memcpy(ssd, bitmap_XNOR, TAMANHO_BITMAP);
                PortaXNOR();
                break;
        }
        render_on_display(ssd, &frame_area); // Essa linha renderiza o bitmap atual no display, ou seja, exibe o bitmap na tela
    }
    return 0;
}

//------------------------------FUNÇÕES AUXILIARES------------------------------------------------
// Funções para as portas lógicas
void PortaAND()
{
    if(gpio_get(botao_A)==1 && gpio_get(botao_B)==1) // Verifica se os dois botões estão soltos, ou seja, se as duas entradas são verdadeiras, uma vez que está em modo pull-up
    {
        // Se sim, acende o LED verde e apaga o vermelho
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
    else // Se não, apaga o LED verde e acende o vermelho
    {
        sleep_ms(100); // Delay para evitar mostrar a cor errada rapidamente antes de conseguir apertar os dois botões ao mesmo tempo
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
}

void PortaOR()
{
    if(gpio_get(botao_A)==1 || gpio_get(botao_B)==1) // Verifica se pelo menos um botão está solto, ou seja, se pelo menos uma entrada é verdadeira
    {
        sleep_ms(100);
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
    else
    {
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
}

void PortaNOT()
{
    // A porta NOT inverte a entrada. Se o botão A estiver solto, ou seja, se a entrada for verdadeira, a saída será falsa
    if (gpio_get(botao_A) == 1)
    {
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
    else //Se não, ou seja, se o botão A estiver pressionado, a saída será verdadeira
    {
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
}

void PortaNAND()
{
    if(gpio_get(botao_A)==1 && gpio_get(botao_B)==1) // Inverte a porta AND. Verifica se os dois botões estão soltos, ou seja, se ambas as entradas são verdadeiras, para que a saída seja falsa
    {
        sleep_ms(100);
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
    else 
    {
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
}

void PortaNOR()
{
    if(gpio_get(botao_A)==1 || gpio_get(botao_B)==1) // A porta NOR inverte a porta OR. Se pelo menos um botão for solto, ou seja, pelo menos uma entrada verdadeira, a saída será falsa.
    {
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
    else // Se não, se ambos os botões estiverem pressionados, a saída será verdadeira
    {
        
        sleep_ms(100);
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
}

void PortaXOR() 
{
    if(gpio_get(botao_A)==1 && gpio_get(botao_B)==0 || gpio_get(botao_A)==0 && gpio_get(botao_B)==1) // Verifica se apenas um botão está solto, ou seja, se apenas uma entrada for verdadeira. As entradas precisam ser diferentes para a saída ser verdadeira.
    {
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
    else
    {
        sleep_ms(100);
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
}

void PortaXNOR()
{
    if(gpio_get(botao_A)==1 && gpio_get(botao_B)==1 || gpio_get(botao_A)==0 && gpio_get(botao_B)==0) // É o inverso da porta XOR. Se ambos os botões estiverem soltos ou ambos pressionados, a saída será verdadeira
    {
        sleep_ms(100);
        gpio_put(LED_G, 1);
        gpio_put(LED_R, 0);
    }
    else
    {
        gpio_put(LED_G, 0);
        gpio_put(LED_R, 1);
    }
}

// Função para exibir a mensagem inicial na tela
void show_message(char *text[])
{
    memset(ssd, 0, ssd1306_buffer_length);
    for (int i = 0; i < 4; i++) {
        ssd1306_draw_string(ssd, 5, i * 16, text[i]);
    }
    render_on_display(ssd, &frame_area);
}

